export const Opaque = (color: string, opacity: number) => {
  const hsla = color.substring(color.indexOf('(') + 1, color.lastIndexOf(')')).split(/,\s*/);

  hsla[3] = opacity.toString();
  return 'hsla(' + hsla.join(', ') + ')';
};
